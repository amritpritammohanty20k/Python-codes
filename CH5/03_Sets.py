''' PROPERTIES OF SET '''
# Set is a collection of unique items (no duplicates allowed).

# It is unordered (items do not have a fixed position).

# Written with curly braces { }.

# Can store different types of data (numbers, strings, etc.).

# You cannot access items using an index (like lists).

# Set is a collection of non repetitve elements .

s=set()
s.add(1)
s.add(4)
print(s)